package UnitTest;

public class HighPricision {
	private  double t = 1;
    public  double add(double d) {
    	return d + t;
    }
    public  double sub(double d) {
    	return t - d;
    }
    public  double mul(double d) {
    	return d * t;
    }
    public double div(double d) {
    	return t  / d;
    }
}
